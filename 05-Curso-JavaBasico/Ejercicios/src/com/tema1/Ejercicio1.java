package com.tema1;

public class Ejercicio1 {

    public static void main(String[] args){
        byte numeroByte = 127;
        System.out.println("Numero byte: " + numeroByte);
        short numeroShort = 32767;
        System.out.println("Numero short: " + numeroShort);
        int numeroEntero = 2000000;
        System.out.println("Numero entero: " + numeroEntero);
        long numeroLong = 1000000000;
        System.out.println("Numero long: " + numeroLong);

        float numeroFloat = 2.25f;
        System.out.println("Numero float: " + numeroFloat);
        double numeroDouble = 105.68d;
        System.out.println("Numero double: " + numeroDouble);

        boolean booleano = true;
        boolean booleano2 = false;
        System.out.println("Booleano: " + booleano);
        System.out.println("Booleano2: " + booleano2);

        String palabra = "palabra";
        System.out.println("String: " + palabra);

    }
}
